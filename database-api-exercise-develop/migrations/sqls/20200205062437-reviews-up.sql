CREATE TABLE IF NOT EXISTS Reviews (id   SERIAL , 
item_id INTEGER REFERENCES Items (id) ON DELETE CASCADE ON UPDATE CASCADE,
 user_id INTEGER REFERENCES Users (id) ON DELETE NO ACTION ON UPDATE CASCADE, 
 rating FLOAT, date DATE, 
 verified BOOLEAN, 
 title VARCHAR(400), 
 body VARCHAR(30000), 
 helpfulVotes INTEGER, createdAt TIMESTAMP NOT NULL DEFAULT NOW(),
 inactive BOOLEAN DEFAULT false, 
 updatedAt TIMESTAMP NOT NULL DEFAULT NOW(), PRIMARY KEY (id));